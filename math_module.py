import math
number= int(input("Enter a number:"))
square_root=math.sqrt(number)
print("Square_root of",number,":",square_root)
factorial=math.factorial(number)
print("Factorial of ",number,":",factorial)
power=math.pow(5,2)
print(number,"raised to 2 ",":",power)
























